#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*cs1;

	cs1 = s1;
	while (*cs1 != '\0')
		cs1++;
	cs1 = ft_strncpy(cs1, s2, n);
	cs1 += n;
	*cs1 = '\0';
	return (s1);
}
